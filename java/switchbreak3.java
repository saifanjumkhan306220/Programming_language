public class switchcase3 {
    public static void main(String[] args) {
        String var = "Saif";

        switch (var) {
            case "Saif" -> System.out.println("hello");
            case "Aayush" -> System.out.println("Namaste");
            case "Harry" -> System.out.println("Bonjour");
            default -> System.out.println("Invalid button");
        }
    }
}
